package newpackage;

public class EncapsulationStudentData {
	private String name;
	private int age;
	private int grade;
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public int getGrade() {
		return grade;
		
	}	
	public void setName(String name) {
		this.name=name;
	}
	public void setAge(int age) {
		this.age=age;
	}
	public void setGrade(int grade) {
		this.grade=grade;
}
}
